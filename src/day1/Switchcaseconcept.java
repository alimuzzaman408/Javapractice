package day1;

public class Switchcaseconcept {

	public static void main(String[] args) {
		char grade='A';
		switch(grade) {
		
     case 'A':System.out.println("GPA 5");break;
     case 'B':System.out.println("GPA 4");break;
     case 'C':System.out.println("GPA 3");break;
     case 'D':System.out.println("GPA 2");break;
    
     default:System.out.println("GPA is not avaiable");
		
		
		
		
		}

	}

}
